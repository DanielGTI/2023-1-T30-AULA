package com.uninove.poo_01;

public class Matematica {
    
    //  Atributos locais
    int a, b;
    
    //  Método Construtor
    public Matematica(int num_a, int num_b){
        a = num_a;
        b = num_b;
    }
    
    //  Método soma
    public int somar(){
        return a + b;
    }
    
    
}
